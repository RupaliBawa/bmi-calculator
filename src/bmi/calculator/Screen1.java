    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        name=jTextField1.getText();     //get name
        String gender;         
        if(jRadioButton1.isSelected()) {
            gender="male";                     //set gender
        }
        else {
            gender="female";                   //set gender
        }        
        double height=Double.parseDouble(jTextField2.getText());         //get height    
        double weight=Double.parseDouble(jTextField3.getText());         //get weight
        
        if(height>1 && weight>=10) {          //validtaions for height and weight
            bmi = weight / (height*height);        
        try {
            //connect to database
            Class.forName("com.mysql.jdbc.Driver");
            if(dbname.equals(null) ) {
                dbname="bmi_users";
            }
            if(dbpass.equals(null)) {
                dbpass="1234";
            }
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/"+dbname,"root",dbpass);
            Statement stmt=con.createStatement();
            
            //create table if does not exists
            stmt.execute("create table if not exists table1(name varchar(48) primary key, gender varchar(16), bmi varchar(24))");
            
            //add values to database
            stmt.executeUpdate("insert into table1(name, gender, bmi) values ('"+name+"','"+gender+"','"+bmi+"')");            
            success=true;
            
        } catch (Exception ex) {
            Logger.getLogger(Screen1.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        else if(weight>=10&&height<1) {
            JOptionPane.showMessageDialog(this, "Height should be greater than 1 m.");
        }
        else if(weight<10&&height>1) {
            JOptionPane.showMessageDialog(this, "Weight should be more than 10 kgs");
        }
        else if(weight<10&&height<1) {
            JOptionPane.showMessageDialog(this, "Height and Weight should be more than 1 m and 10 kgs respectively!");
        }
        
        
      if(success) {
          DecimalFormat df=new DecimalFormat("###0.00");
          jLabel8.setText("Your bmi is : "+ df.format(bmi));
          
          
        if(bmi<18.5) {
            jLabel8.setText(jLabel8.getText()+". You are Underweight.");
            state = "Underweight";
        }
        else if(bmi>18.5&&bmi<24.9) {
            jLabel8.setText(jLabel8.getText() + ". You are Healthy.");
            state = "Healthy";
        }
        else if(bmi>29.9&&bmi<24.9) {
            jLabel8.setText(jLabel8.getText() + ". You are Overweight.");
            state = "Overweight";            
        }
        else {            
            jLabel8.setText(jLabel8.getText() + ". You are Obese.");
            state = "Obese";
        }
        
        suggestions();
          
      }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

