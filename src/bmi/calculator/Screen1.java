    private void viewrecords() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            if(dbname.equals(null)) {
                dbname = "bmi_users";
            }
            if(dbpass.equals(null)) {
                dbpass = "1234";
            }
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/"+dbname,"root",dbpass);
            PreparedStatement pst=conn.prepareStatement("select * from table1");
            ResultSet rs=pst.executeQuery();            
            int i=1;
            
            DefaultListModel<String> sr=new DefaultListModel<>();
            DefaultListModel<String> name=new DefaultListModel<>();
            DefaultListModel<String> gender=new DefaultListModel<>();
            DefaultListModel<String> bmi=new DefaultListModel<>();
            
            while(rs.next()) {
                sr.addElement(""+i); i++;
                name.addElement(rs.getString("name"));
                gender.addElement(rs.getString("gender"));
                bmi.addElement(rs.getString("bmi"));
            }
            
            jList1.setModel(sr);
            jList2.setModel(name);
            jList3.setModel(gender);
            jList4.setModel(bmi);
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }
